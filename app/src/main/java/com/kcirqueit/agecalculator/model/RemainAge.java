package com.kcirqueit.agecalculator.model;

public class RemainAge {

    private int day;
    private int month;

    public RemainAge(int day, int month) {
        this.day = day;
        this.month = month;
    }

    public int getDay() {
        return day;
    }

    public int getMonth() {
        return month;
    }
}
